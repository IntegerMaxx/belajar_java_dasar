package com.tutorial;


// Operator assigment
public class Tutorial13 {
    
    public static void main(String[] args){
        
        // Assigment operator jumlah

        // Asigment itu tanda (sama dengan '=')
        
        int a = 1;
        
        a +=10; // sama saja dengan > a = a + 10;
        
        
        System.out.println("nilai a = " + a);
        
        // Assigment operator pengurangan
        int b = 100;
        b -= 25; 
        System.out.println("nilai b = " + b);
        
        // Assigment operator perkalian
        int c = 100;
        c *= 20; 
        System.out.println("nilai c = " + c);
        
        // Assigment operator pembagian
        int d = 100;
        d /= 20; 
        System.out.println("nilai d = " + d);
        
        // Assigment operator modulus
        int e = 10;
        e %= 7; 
        System.out.println("nilai e = " + e);
    }
    
}
